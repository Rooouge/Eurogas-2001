package it.eurogas2001.components.fileinformation;

public class InformationClass {

	public int index;
	public String name;
	public String address;
	public String town;
	public String province;
	public int cap;
	
	public InformationClass(int index, String name, String address, String town, String province, int cap) {
		this.index = index;
		this.name = name;
		this.address = address;
		this.town = town;
		this.province = province;
		this.cap = cap;
	}
}
